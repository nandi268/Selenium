package javascript;
// Converting one data type to other data type 

public class TypeCasting {

	public static void main(String[] args) {

		int a = 10;

		double b = a;

		System.out.println("Typecasted value is " + b);

		double x = 10.5;

		int y = (int) x;

		System.out.println("Typecasted value is" + y);
	}

}
